package com;

public class SingleDimensionalArray {

	public static void main(String[] args) {
		int[] a=new int[3];  
		a[0]=15;
		a[1]=25;  
		a[2]=35;  
  
		System.out.println("One dimensional array elements are");    
		System.out.println(a[0]);    
		System.out.println(a[1]);    
		System.out.println(a[2]);  
	}

}